export function LangProvider({ children }) {
  const [lang, setLangState] = useState(() => localStorage.getItem('ss_lang') || 'en');
  const setLang = useCallback((code) => {
    localStorage.setItem('ss_lang', code);
    setLangState(code);
  }, []);
  const t = useCallback((key) => T[lang]?.[key] ?? T.en[key] ?? key, [lang]);
  return <LangContext.Provider value={{ lang, t, setLang }}>{children}</LangContext.Provider>;
}

export function useLang() { return useContext(LangContext); }
